    def load_make_model_seat(self):
      import pandas as pd
      csv_path = "/var/www/html/car_scrapers/modules/SEAT_PREDICTION/SEAT_PREDICTION_ML.csv"
      data = pd.read_csv(csv_path)
      all_data = []
      for index,row in data.iterrows():
        key = "{};{};{}".format(str(row['make']).strip().lower(),str(row['model']).strip().lower(),str(row['seats']).strip())
        value = "{} {}".format(str(row['make']).strip().lower(),str(row['model']).strip().lower())
        all_data.append({"key":key,"value":value})
      return all_data

    def get_vectorizer(self,all_data):
      all_strings = []
      for row in all_data:
          all_strings.append(row['value'])
      from sklearn.feature_extraction.text import TfidfVectorizer
      vectorizer = TfidfVectorizer(analyzer="char_wb")
      vectorizer.fit(all_strings)
      return vectorizer

    def get_seats_from_key(self,key):
      temp = key.split(";")
      return str(int(float(temp[-1])))


    def get_tfidf(self,all_data,vectorizer):
      final_out = []
      all_values = []
      all_keys = []
      for row in all_data:
        all_values.append(row['value'])
        all_keys.append(row['key'])
        
      all_tfidf = vectorizer.transform(all_values).todense().tolist()
      for k,v,t in zip(all_keys,all_values,all_tfidf):
        final_out.append({"key":k,"value":v,"tfidf":t})
      return final_out

    def cosine_similarity(self,list_1, list_2):
      from numpy import dot
      from numpy.linalg import norm
      cos_sim = dot(list_1, list_2) / (norm(list_1) * norm(list_2))
      return cos_sim

    def predict_seats(self,all_tfidf,vectorizer,make,model):
      import pandas as pd
      make_model_str = "{} {}".format(str(make).strip().lower(),str(model).strip().lower())
      input_tfidf = vectorizer.transform([make_model_str]).todense().tolist()[0]
      df = pd.DataFrame(all_tfidf)
      df['cosim'] = df['tfidf'].apply(lambda x:self.cosine_similarity(input_tfidf,x))
      max_row = df[df['cosim'] == df['cosim'].max()]
      if max_row['cosim'].to_list()[0] > 0.87:
        return self.get_seats_from_key(max_row['key'].to_list()[0]),max_row['cosim'].to_list()[0]
      else:
        return None,max_row['cosim'].to_list()[0]
